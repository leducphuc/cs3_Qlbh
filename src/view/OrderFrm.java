    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ResultSet rs = null;
        ProductDAO od = new ProductDAO();
        if(txtSearch.getText().equals("")){
            if(cbxType.getSelectedIndex()==0){
                rs = od.search();
            }else{
                String type = cbxType.getSelectedItem().toString();
                rs = od.searchType(type);
            }
        }else{
            if(cbxType.getSelectedIndex()==0){
                String text = txtSearch.getText();
                rs = od.searchText(text);
            }else{
                String type = cbxType.getSelectedItem().toString();
                String text = txtSearch.getText();
                rs = od.searchTypeText(type, text);
            }
        }
        showtblPro(rs);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        int erro = 0;
        if(txtQuantity.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap so luong can mua!");
            erro = 1;
        }
        
        if(erro == 0){
            DefaultTableModel model = (DefaultTableModel) tblPro.getModel();
        int row = tblPro.getSelectedRow();
        
        int ID = Integer.parseInt(model.getValueAt(row, 0).toString());
        String name = model.getValueAt(row, 1).toString();
        int quantity = Integer.parseInt(txtQuantity.getText());
        double price = Double.parseDouble(model.getValueAt(row, 3).toString());
        double thanhtien = quantity*price;
        
        DefaultTableModel model2 = (DefaultTableModel) tblOrder.getModel();
        model2.addRow(new Object[]{ID, name, price, quantity, thanhtien});
        tblOrder.setModel(model2);
        txtQuantity.setText("");
        }
    }//GEN-LAST:event_btnOrderActionPerformed

