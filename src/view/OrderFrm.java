    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int erro = 0;
        if(txtID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap ID khach hang!");
            erro = 1;
        }else if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap ten khach hang!");
            erro = 1;
        }
        
        if(erro == 0){
            int yes = 0;
            int ID = Integer.parseInt(txtID.getText());
            CustomersDAO cd = new CustomersDAO();
            ResultSet rs = cd.searchID();
            try {
                while(rs.next()){
                    if(rs.getInt("ID") == ID){
                        yes = 1;
                        break;
                    }
                }
            } catch (SQLException ex) {
                System.out.println(ex);
            }
           
            String name = txtName.getText();
            Customers c = new Customers();
            c.setID(ID);
            c.setName(name);
            if(yes == 0){
                cd.Add(c);
            }else if(yes == 1){
                cd.Update(c);
            }           
            JOptionPane.showMessageDialog(null, "Successfull");       
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int erro = 0;
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        if(model.getDataVector().isEmpty()){
            JOptionPane.showMessageDialog(null, "Gio hang trong!");
            erro = 1;
        }else if(txtID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap ID khach hang!");
            erro = 1;
        }else if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap ten khach hang!");
            erro = 1;
        }

        if(erro == 0){
            //Them orders vao csdl
        Calendar c = Calendar.getInstance();
        Date date = c.getTime();
        
        double tongtien = 0;
        int numRow = tblOrder.getRowCount();
        for(int i=0;i<numRow;i++){
            tongtien += Double.parseDouble(model.getValueAt(i, 4).toString());
        }       
        
        int cus_ID = Integer.parseInt(txtID.getText());
        Orders o = new Orders(date, tongtien, cus_ID);
        OrdersDAO cd = new OrdersDAO();
        cd.Add(o);
        
        //Them orderdetails vao csdl
        for(int i=0;i<numRow;i++){
            int quantity = Integer.parseInt(model.getValueAt(i, 3).toString());
            double sumPrice = Double.parseDouble(model.getValueAt(i, 4).toString());
            CustomersDAO cus = new CustomersDAO();
            ResultSet rs = cus.searchID_Orders(Integer.parseInt(txtID.getText()));
            int orders_ID = 0;
            try {
                while(rs.next()){
                    orders_ID = rs.getInt("id");
                }
            } catch (SQLException ex) {
                System.out.println(ex);
            }            
            int pro_ID = Integer.parseInt(model.getValueAt(i, 0).toString());
            
            Orderdetails ordetail = new Orderdetails(sumPrice, quantity, pro_ID, orders_ID);
            OrderdetailsDAO od = new OrderdetailsDAO();
            od.Add(ordetail);                      
        }
        
        txtTongTien.setText(String.valueOf(tongtien));
        JOptionPane.showMessageDialog(null, "Successfull");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

