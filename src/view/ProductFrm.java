    public void showtblProduct(){
        ProductDAO pd = new ProductDAO();
        ResultSet rs = pd.search();
        DefaultTableModel model = (DefaultTableModel) tblUpdatePro.getModel();
        model.getDataVector().removeAllElements();
        try {
            while(rs.next()){
                model.addRow(new Object[]{rs.getInt("ID"), rs.getString("name"), rs.getString("image"), rs.getDouble("price"),
                rs.getInt("quantity"), rs.getString("des"), rs.getString("status"), rs.getDate("adddate"), rs.getString("type")});
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        tblUpdatePro.setModel(model);
    }
    
    public void loadcbxType(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbxType.getModel();
        CategoriesDAO cd = new CategoriesDAO();
        ResultSet rs = cd.searchName();
        try {
            while(rs.next()){
                model.addElement(rs.getString("name"));
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        cbxType.setModel(model);
    }
    
