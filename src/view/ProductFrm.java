    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int erro = 0;
        if(txtID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap ID!");
            erro = 1;
        }else if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap Name!");
            erro = 1;
        }else if(txtPrice.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap Price!");
            erro = 1;
        }else if(txtQuantity.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap Quantity!");
            erro = 1;
        }else if(txtStatus.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nhap Status!");
            erro = 1;
        }
        
        int id = Integer.parseInt(txtID.getText());
        ProductDAO pd = new ProductDAO();
        ResultSet rs = pd.searchID();
        try {
            while(rs.next()){
                if(id == rs.getInt("ID")){
                    erro = 1;
                    JOptionPane.showMessageDialog(null, "ID da ton tai!");
                    break;
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        
        if(erro == 0){      
            String name = txtName.getText();
            String type = (String) cbxType.getSelectedItem();
            double price = Double.parseDouble(txtPrice.getText());
            int quantity = Integer.parseInt(txtQuantity.getText());
            Date date = null;
            try {
                date = new SimpleDateFormat("dd/MM/yyyy").parse(txtDate.getText());
            } catch (ParseException ex) {
                System.out.println(ex);
            }
            String status = txtStatus.getText();
            String image = txtImage.getText();
            String describe = taDescribe.getText();
               
            Product pro = new Product(id, name, image, price, quantity, describe, status, date);
        
            CategoriesDAO cd = new CategoriesDAO();
            rs = cd.searchID(type);
            int cate_ID = 0;
            try {
                while(rs.next()){
                    cate_ID = rs.getInt("ID");
                }
            } catch (SQLException ex) {
                System.out.println(ex);
            }
            pro.setCate_ID(cate_ID);
            pd.Add(pro);
            JOptionPane.showMessageDialog(null, "Successfull");
            showtblProduct();
            Cancel();
        }      
    }//GEN-LAST:event_jButton2ActionPerformed

